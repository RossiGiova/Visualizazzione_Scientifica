df = pd.DataFrame(efficacy_matrix, index=types, columns=types)

G = nx.DiGraph()

for i, att_type in enumerate(types):
    for j, def_type in enumerate(types):
        efficacy = df.iloc[i, j]
        G.add_edge(att_type, def_type, weight=efficacy)

plt.figure(figsize=(14, 12))

pos = nx.circular_layout(G)

edge_colors = []
for u, v in G.edges():
    efficacy = G[u][v]['weight']
    if efficacy == 0.0:
        edge_colors.append('#c1516b')  # Inefficace
    elif efficacy == 0.5:
        edge_colors.append('#ea856d')   # Poco efficace
    elif efficacy == 1.0:
        edge_colors.append('#eccc80')  # Normale
    elif efficacy == 2.0:
        edge_colors.append('#519877') # Superefficace

nx.draw(G, pos, with_labels=True, node_size=5000, node_color='lightblue', font_size=10, font_weight='bold', edge_color=edge_colors, width=2, arrows=True)

